export const Util = {
	bintohex: function(b){
		var output = '';
					for (var i = 0; i < b.length; i += 4) {
							var bytes = b.substr(i, 4);
							var decimal = parseInt(bytes, 2);
							var hex = decimal.toString(16);
							output += hex;
					}
					return output;
	},
	hextobin: function(h){
		var output = '';
		for (var i = 0; i < h.length; i++) {
			var decimal = parseInt(h.charAt(i), 16);
			var binary = decimal.toString(2)
			while (binary.length < 4) {
	          binary = "0" + binary;
	      }
			output += binary;
		}
		return output;
	},
	dectobin: function(d){
		return Util.hextobin(Util.dectohex(d));
	},
	bintodec: function(b){
		return Util.hextodec(Util.bintohex(b));
	},
	hextodec: function(h){
		return parseInt(h, 16);
	},
	dectohex: function(d, padding){
		var hex = Number(d).toString(16);
		padding = typeof (padding) === "undefined" || padding === null ? padding = 2 : padding;
		while (hex.length < padding) {
				hex = "0" + hex;
		}
		return hex;
	},
	hextoascii: function(h){
		var hex = h.toString();
		var str = '';
		for (var i = 0; i < hex.length; i += 2)
				str += String.fromCharCode(parseInt(hex.substr(i, 2), 16));
		return str;
	},
	dectoascii: function(d){
		return Util.hextoascii(Util.dectohex(d))
	},
	bintoascii: function(b){
		return Util.hextoascii(Util.bintohex(b))
	},
	reversebin: function(input){
		var out = "";
		for(var i = 0; i <= input.length - 8; i += 8){
			var t = input.substr(i, 8);
			out += t.split("").reverse().join("");
		}
		return out;
	}


}

